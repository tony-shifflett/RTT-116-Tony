package PA303_2_1;

public class IntDivision {
    public static void main(String[] args) {
        int firstInt = 20;
        int secondInt = 100;
        int result = secondInt / firstInt;
        System.out.println(result);

        double newOperand = 33.6;
        double newResult = newOperand / firstInt;
        System.out.println(newResult);
    }
}
